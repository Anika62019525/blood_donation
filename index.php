            </div>
            <div class="row">
                <div class="col-md-6 text">
                    <h1>About Blood Doners</h1>
                    <h3>What is it?</h3>
                    <p>
                        Blood Collected straight from the donor into a blood bag and mixed with an anticoagulant is
                        called as whole blood. This collected whole blood is then centrifuged and red cell, platelets
                        and plasma are separated. The separated Red cells are mixed with a preservative to be called as
                        packed red blood cells.</p>
                    <h3>Who can donate?</h3>
                    <p>
                        You need to be 18-65 years old, weight 45kg or more and be fit and healthy..</p>
                    <h3>User For?</h3>
                    <p>
                        Correction of severe anemia in a number of conditions and blood loss in case of child birth,
                        surgery or trauma settings</p>
                    <h3>Lasts For?</h3>
                    <p>
                        Red cells can be stored for 42 days at 2-6 degree celsius.</p>
                    <h3>How long does it take to donate?</h3>
                    <p>15-30 minutes to donate.including the pre-donation check-up.</p>
                    <h3>How often can I donate?</h3>
                    <p>Male donors can donate again after 90 days and female donors can donate again after 120 days.</p>
